<?php
	echo 'Hello, world!';
	print 'Hello, world!';
?>
<?= 'Hello, world'
	/* короткий echo-тег, который является сокращением для
	более многословного <?php echo ?> */
?>

<? echo 'Hello, world!'; // сокращённая версия   ?>

<h1>Пример встраивания PHP в HTML</h1>
